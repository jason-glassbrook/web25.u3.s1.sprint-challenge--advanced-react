/*--------------------------------------
  branch
--------------------------------------*/
export const branch = ($name, $pack) => {
  const $pathname = ($pack) ? ($pack.$pathname + '/') : ('');
  const $fullname = $pathname + $name;

  return ({ $name, $pathname, $fullname });
};

/*--------------------------------------
  top
--------------------------------------*/
export const top = ($name) => (branch ($name));

/*//////////////////////////////////////
  exports
//////////////////////////////////////*/
export default ({ branch, top });
